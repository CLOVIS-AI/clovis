package clovis.ui.composables.buttons

/**
 * Semantic buttons.
 *
 * We split buttons into the following categories:
 * - **Action buttons**,
 * - **Regular buttons**.
 *
 * Action buttons are generally more important in the UI, and represent actions that the user should consider in priority.
 * For example, they are used for confirmation popups.
 * - [PrimaryActionButton] is the most important button of the page.
 * - [SecondaryActionButton] is used for all other important buttons.
 *
 * Regular buttons represent all other actions a user can make.
 * - [EmphasizedButton] can be used to highlight the most important button in a group.
 * - [Button] is a normal simple button.
 */
typealias ButtonDocumentation = Nothing
